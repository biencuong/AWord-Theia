// QUAN TRỌNG: nls.localizeByDefault của Theia CHUẨN HÓA chuỗi (bỏ '&&' — ký hiệu
// phím tắt menu của VS Code) trước khi tra khóa, và bản đồ tra cứu được ghi đè
// theo thứ tự duyệt nên mục XUẤT HIỆN SAU CÙNG thắng. Vì vậy phải khớp từ điển
// theo chuỗi ĐÃ chuẩn hóa với MỌI vị trí (vd '&&View' lẫn 'View'), nếu không các
// nhãn menu chính (View/Go/Help...) sẽ trượt khóa hiệu lực và giữ tiếng Anh.
Object.assign(TU_DIEN, TU_DIEN_BO_SUNG);

const normalize = s => s.replace(/&&/g, '');
const TU_DIEN_CHUAN = new Map();
for (const [en, vi] of Object.entries(TU_DIEN)) {
    TU_DIEN_CHUAN.set(normalize(en), normalize(vi));
}

const meta = JSON.parse(fs.readFileSync(metaPath, 'utf8'));
const existing = JSON.parse(fs.readFileSync(outPath, 'utf8'));
const contents = existing.contents || {};

let addedEntries = 0;
const matchedEn = new Set();
for (const [file, msgs] of Object.entries(meta.messages)) {
    const keys = meta.keys[file];
    for (let i = 0; i < msgs.length; i++) {
        const norm = normalize(msgs[i]);
        const vi = TU_DIEN_CHUAN.get(norm);
        if (vi === undefined) continue;
        matchedEn.add(norm);
        const key = typeof keys[i] === 'string' ? keys[i] : keys[i].key;
        contents[file] ??= {};
        if (!(key in contents[file])) {
            contents[file][key] = vi;
            addedEntries++;
        }
    }
}

const notFound = [...TU_DIEN_CHUAN.keys()].filter(en => !matchedEn.has(en));
if (notFound.length) {
    console.warn(`[gen-i18n-vi] ${notFound.length} chuỗi trong từ điển KHÔNG khớp metadata (kiểm tra chính tả):`);
    for (const s of notFound) console.warn('  -', JSON.stringify(s));
}

existing.contents = contents;
fs.writeFileSync(outPath, JSON.stringify(existing, null, 2), 'utf8');

let totalEntries = 0;
for (const f of Object.keys(contents)) totalEntries += Object.keys(contents[f]).length;
console.log(`[gen-i18n-vi] Thêm ${addedEntries} mục mới; tổng ${totalEntries} mục dịch trên ${Object.keys(contents).length} tệp.`);
